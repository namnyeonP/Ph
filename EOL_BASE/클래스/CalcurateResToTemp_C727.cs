        /// <summary>
        /// C727 온도 가져오는 함수
        /// </summary>
        /// <param name="Resistance">Kohm단위여야함 ex)10.732</param>
        /// <param name="Temp">out Value, 온도 가져오기</param>
        /// <returns></returns>
        public int Calculate(double Resistance, out double Temp)
        {
            SetRMinTable();
            //SetRNorTable();
            //SetRMaxTable();

            Temp = 0;
            try
            {
                if (Resistance < 0.600d)
                {
                    Resistance = 0.600d;
                }

                if (Resistance > 204.5d)
                    Resistance = 204.5d;

                if (_TemperatureTable.ContainsKey(Resistance))
                {
                    Temp = _TemperatureTable[Resistance];
                    return 0;
                }
                else
                {
                    double fVal = 0;
                    double sVal = 0;
                    foreach (double key in _TemperatureTable.Keys)
                    {
                        if (key > Resistance)
                        {
                            sVal = key;
                            break;
                        }
                        else
                        {
                            fVal = key;
                        }
                    }
                    //Temperature.Add(_TemperatureTable[sVal] + ((sVal - TemperVoltage[i]) / (sVal - fVal)));
                    /// (온도높은놈 - 온도낮은놈) / (저항낮은놈 - 저항높은놈)  * (순수저항값 - 저항높은놈) + 온도낮은놈
                    /// 
                    var 온도높은놈 = _TemperatureTable[fVal];
                    var 온도낮은놈 = _TemperatureTable[sVal];
                    var 저항낮은놈 = fVal;
                    var 저항높은놈 = sVal;
                    var 순수저항값 = Resistance;

                    Temp = (온도높은놈 - 온도낮은놈) / (저항낮은놈 - 저항높은놈) * (순수저항값 - 저항높은놈) + 온도낮은놈;

                    LogState(LogType.Info, string.Format("({0} - {1}) / ({2} - {3}) * ({4} - {5}) + {6} = {7}",
                        온도높은놈, 온도낮은놈, 저항낮은놈, 저항높은놈, 순수저항값, 저항높은놈, 온도낮은놈, Temp));

                    //Temp = _TemperatureTable[sVal];// + ((sVal - Resistance) / (sVal - fVal));
                    return 0;
                }
            }
            catch (Exception ex)
            {
                LogState(LogType.Info, "온도 변환 실패!!");
            }
            return -1;  
        }

